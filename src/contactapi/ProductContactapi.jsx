/*< --- Step 1: Create a Context ----->
Use React.createContext() to create a context.*/

/*< --- Step 2: Create a Provider Component ----->
Create a Provider component that will wrap your application and provide the context value.*/

/*< --- Step 3: Wrap Your Application with the Provider ----->
Wrap your application (or a part of it) with the Provider component so that all child components can access the context.*/

/*< --- Step 4: Consume the Context in Child Components ----->
Use the useContext hook to access the context data in functional components.*/